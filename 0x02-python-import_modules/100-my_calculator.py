def add(a, b):
    """
    My addition function

    Args:
        a: first integer
        b: second integer

    Returns:
        The sum of a + b
    """
    return (a + b)


def sub(a, b):
    """
    Subtraction function

    Args:
        a: first integer
        b: second integer

    Returns:
        The difference a - b
    """
    return (a - b)


def mul(a, b):
    """
    Multiplication function

    Args:
        a: first integer
        b: second integer

    Returns:
        The product of a * b
    """
    return (a * b)


def div(a, b):
    """
    Division function

    Args:
        a: first integer
        b: second integer

    Returns:
        The value of a / b
    """
    return int(a / b)
